#include "libft.h"

int	ft_putstr_int(char *s)
{
	size_t	i;

	if (!s)
		return (ft_putstr_int("(null)"));
	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}
